package newjavalearn;

public class Stringlearning 
{
	public static void main(String args[])
	{
		int a=200;
		int b=300;
		
		String x="hello";
		String y="world";
		System.out.println(a+b+x+y);
		System.out.println(x+y+a+b);
		
		System.out.println(x+y+(a+b));
		System.out.println(a+b+(x+y));
	

	if(a>b)
	{
		System.out.println("a is  best");
	}
	else
	{
		System.out.println("b is best");
	}
	
}
}